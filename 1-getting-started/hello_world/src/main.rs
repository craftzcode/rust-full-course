//! Main Function
// (main) function is the entry point to the program. Rust will automatically execute the main function first
// It's going to look in our code for the (main) function definition and run that automatically
fn main() {
    //* Macro
    // Think of (macro) just like a function. It's a procedure that we can utilize
    // It's somebody else's code that already been written that's going to be responsible for something else
    // In this case, the technical responsibility of outputting text to the screen
    // This macro is called print line (println!)
    println!("Hello, world!"); // (;) semicolons terminate the line statement. You can think of it like the period at the end of a sentence
}
