//! Constant
// Similar to a variable
// It is a name assigned to a value. A constant's value cannot change
// Can be declared at any scope, including at the file level

// Constant names is use all capital letters with underscores in between subsequent
const TAX_RATE: f64 = 7.25;

fn main() {
    println!("The tax rate is {TAX_RATE}");
}
